// Helper functions
export function getServicesByCategory(category) {
  return complianceServices.filter(s => s.category === category);
}

export function getServiceById(id) {
  return complianceServices.find(s => s.id === id);
}

export function getCategoryDisplayName(category) {
  const names = {
    certification: 'Certifications',
    annual_compliance: 'Annual Compliances',
    post_registration: 'Post-Registration Changes',
  };
  return names[category] || category;
}
